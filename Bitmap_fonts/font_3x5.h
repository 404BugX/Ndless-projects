static inline void draw_char3(SDL_Surface *screen, char c,
                                int x, int y, Uint32 color,
                                const BitmapFont3 *font) {
    if (c < 32 || c > 126) return;
    const unsigned char *glyph = font->data[c - 32];
    for (int row = 0; row < font->h; row++) {
        for (int col = 0; col < font->w; col++) {
            if (glyph[row] & (1 << (font->w - 1 - col))) {
                SDL_FillRect(screen,
                    &(SDL_Rect){x + col, y + row, 1, 1},
                    color);
            }
        }
    }
}

static inline void draw_text3(SDL_Surface *screen, const char *str,
                                int x, int y, Uint32 color,
                                const BitmapFont3 *font) {
    int cx = x;
    while (*str) {
        if (*str == '\n') { cx = x; y += font->h + 1; }
        else { draw_char3(screen, *str, cx, y, color, font); cx += font->w + 1; }
        str++;
    }
}

